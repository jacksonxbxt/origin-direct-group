// Helper to get unique product list
export function getUniqueProducts() {
  const seen = new Set<string>();
  return tradeRoutes.filter((r) => {
    if (seen.has(r.productSlug)) return false;
    seen.add(r.productSlug);
    return true;
  }).map((r) => ({ slug: r.productSlug, name: r.productName, origin: r.originCountries }));
}

// Helper to get routes for a specific product
export function getRoutesForProduct(productSlug: string) {
  return tradeRoutes.filter((r) => r.productSlug === productSlug);
}
